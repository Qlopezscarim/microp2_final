// Param uint8_t "data": data to be sent.
// Return: void
void ST7789_WriteData(uint8_t data) {
    SPI_WriteSingle(SPI_A_BASE, data);
}

// ST7789_ReadRegister
// Reads from SPI bus.
// Return: uint8_t
uint8_t ST7789_ReadRegister(uint8_t data) {
    return SPI_ReadSingle(SPI_A_BASE);
}

